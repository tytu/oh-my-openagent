const metisRestrictions = createAgentToolRestrictions([
  "write",
  "edit",
  "task",
  "delegate_task",
])

export function createMetisAgent(model: string): AgentConfig {
  return {
    description:
      "预规划顾问，分析请求以识别隐藏意图、歧义和 AI 失败点。",
    mode: "subagent" as const,
    model,
    temperature: 0.3,
    ...metisRestrictions,
    prompt: METIS_SYSTEM_PROMPT,
    thinking: { type: "enabled", budgetTokens: 32000 },
  } as AgentConfig
}


export const metisPromptMetadata: AgentPromptMetadata = {
  category: "advisor",
  cost: "EXPENSIVE",
  triggers: [
    {
      domain: "预规划分析",
      trigger: "需要范围澄清的复杂任务、模糊的需求",
    },
  ],
  useWhen: [
    "在规划非平凡任务之前",
    "当用户请求模糊或开放式时",
    "为防止 AI 过度工程模式",
  ],
  avoidWhen: [
    "简单的、明确的任务",
    "用户已经提供了详细的需求",
  ],
  promptAlias: "Metis",
  keyTrigger: "模糊或复杂的请求→在 Prometheus 之前咨询 Metis",
}
